/** Build 10–15 demo units for one hub. Keeps legacy mock booking vehicle ids. */
function buildSiteFleet(opts: {
  siteId: string;
  operatorId: string;
  lat: number;
  lng: number;
  idPrefix: string;
  units: { modelId: string; codePrefix: string; count: number; start: number }[];
}): Vehicle[] {
  const out: Vehicle[] = [];
  const batts = [94, 88, 81, 76, 72, 68, 91, 85, 79, 73, 96, 84];
  let bi = 0;
  for (const group of opts.units) {
    for (let i = 0; i < group.count; i++) {
      const n = group.start + i;
      const codeNum = String(n).padStart(2, "0");
      const short = group.codePrefix.split("-").pop()!.toLowerCase();
      let id = `v-${opts.idPrefix}-${short}${n}`;
      if (opts.siteId === "site-margonda-lobby" && group.modelId === "m-margo-galaxy" && n === 1) {
        id = "v-mg-g1";
      }
      if (opts.siteId === "site-jakarta-tebet" && group.modelId === "m-margo-ledo" && n === 1) {
        id = "v-mg-l1";
      }
      if (opts.siteId === "site-margonda-lobby" && group.modelId === "m-margo-m110" && n === 1) {
        id = "v-mg-m1";
      }
      out.push(
        unit({
          id,
          modelId: group.modelId,
          operatorId: opts.operatorId,
          siteId: opts.siteId,
          code: `${group.codePrefix}${codeNum}`,
          status: "available",
          batteryPct: batts[bi++ % batts.length],
          lat: opts.lat + (i % 3) * 0.0001,
          lng: opts.lng + (i % 2) * 0.0001,
        }),
      );
    }
  }
  return out;
}

