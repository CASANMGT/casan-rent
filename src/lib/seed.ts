function unit(
  partial: Omit<
    Vehicle,
    | "name"
    | "vehicleType"
    | "rentalMode"
    | "allowFrontDesk"
    | "allowSelfService"
    | "motorWatts"
    | "rangeKm"
    | "maxSpeedKmh"
    | "weightKg"
    | "batteryVoltageV"
    | "batteryAh"
    | "chargerAmpsDefault"
    | "pricePerHour"
    | "emoji"
    | "requiresSimAck"
    | "color"
    | "colorHex"
  > & { modelId: string; batteryPct?: number | null; color?: string; colorHex?: string },
): Vehicle {
  const model = vehicleModels.find((m) => m.id === partial.modelId)!;
  const isBike = model.vehicleType === "bicycle";
  const palette = FLEET_COLORS;
  const tone = palette[Math.abs(hashCode(partial.code)) % palette.length];
  return {
    ...partial,
    name: model.name,
    vehicleType: model.vehicleType,
    rentalMode: model.rentalMode,
    allowFrontDesk: model.allowFrontDesk,
    allowSelfService: model.allowSelfService,
    motorWatts: model.motorWatts,
    rangeKm: model.rangeKm,
    maxSpeedKmh: model.maxSpeedKmh,
    weightKg: model.weightKg,
    batteryVoltageV: model.batteryVoltageV,
    batteryAh: model.batteryAh,
    chargerAmpsDefault: model.chargerAmpsDefault,
    batteryPct: isBike ? null : (partial.batteryPct ?? 80),
    pricePerHour: model.pricePerHour,
    emoji: model.emoji,
    requiresSimAck: model.requiresSimAck,
    color: partial.color ?? tone.color,
    colorHex: partial.colorHex ?? tone.colorHex,
  };
}

function hashCode(s: string): number {
  let h = 0;
  for (let i = 0; i < s.length; i++) h = (h * 31 + s.charCodeAt(i)) | 0;
  return h;
}

